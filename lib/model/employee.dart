class Employee {
  final String name; //員工姓名
  final String title; //職稱
  final String contact; //連絡電話
  final String address; //地址
  final String joinDate; //入職日
  final String serviceYears; //年資

  Employee({
    required this.name,
    required this.title,
    required this.contact,
    required this.address,
    required this.joinDate,
    required this.serviceYears,
  });
}